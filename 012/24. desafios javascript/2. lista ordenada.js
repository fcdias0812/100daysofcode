// Crie uma lista com os números 1, 3, 5, 7, 0, 9
let lista = [1, 3, 5, 7, 0, 9];
console.log(lista);

// Ordene a lista do menor para o maior
lista.sort();
console.log(lista);

// Retirar o primeiro número da lista
lista.splice(1, 1);
console.log(lista);

// Inverter toda a ordem
lista.reverse();
console.log(lista);